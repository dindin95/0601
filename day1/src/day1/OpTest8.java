package day1;

public class OpTest8 {
	
	public static void main(String[] args) {
		//단항 연산자  a+b
		
		int su = 10;
		System.out.println(su);
		++su; // su= su+ 1 
		System.out.println(su);
		++su;
		System.out.println(su);
		--su; //su= su-1
		System.out.println(su);
		--su;
		System.out.println(su);
		


	}

}
