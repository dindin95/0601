package day1;

public class OpTest7 {
	
	public static void main(String[] args) {
		

		int su =10;
		
		su+=2;
		System.out.println(su); //12
		su-=1;
		System.out.println(su);//11
		su*=2;
		System.out.println(su);//22
		su/=2;
		System.out.println(su);//11
		su%=2;
		System.out.println(su);//나머지값 
		
	}

}
