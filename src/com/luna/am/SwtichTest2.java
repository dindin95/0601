package com.luna.am;

import java.util.Scanner;

public class SwtichTest2 {
	
	public static void main(String[] args) {
		

		char ch ='a';
		
		switch(ch) {
		case 'a' : 
			System.out.println("문자 a 입니다");
			break;
		case 'b' : 
			System.out.println("문자 b 입니다");
			break;
		case 'c' : 
			System.out.println("문자 c 입니다");
			break;
			default:
				System.out.println("나머지 문자");
		}
		
			
	}

}
